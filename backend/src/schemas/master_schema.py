class OutOfNetwork(BaseModel):
    individual_deductible: Optional[str] = Field(description="OON Deductible", default=None)
    family_deductible: Optional[str] = Field(description="OON Deductible", default=None)
    individual_oop_max: Optional[str] = Field(description="OON OOP Max", default=None)
    family_oop_max: Optional[str] = Field(description="OON OOP Max", default=None)
    out_of_network_coverage: Optional[bool] = Field(default=None, description="Out Of Network Coverage checkbox flag")
