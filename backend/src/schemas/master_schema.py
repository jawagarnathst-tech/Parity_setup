from pydantic import BaseModel, Field
from typing import Optional

class PlanInformation(BaseModel):
    plan_name: Optional[str] = Field(description="Name of the plan")
    carrier: Optional[str] = Field(description="Carrier Name e.g. Aetna, UHC. Must match template dropdowns if possible.")
    plan_source: Optional[str] = Field(default=None, description="PEO or Source (e.g. ADP, TriNet, Justworks)")
    plan_type: Optional[str] = Field(description="e.g. EPO, HMO, PPO - Based")
    specialty_network: Optional[str] = Field(default=None, description="Specialty network name or status")
    zip_code: Optional[str] = Field(default=None)
    renewal_date: Optional[str] = Field(default=None)
    hdhp: Optional[bool] = Field(default=None, description="High deductible health plan flag")
    open_access: Optional[bool] = Field(default=None, description="Open access plan flag")
    plan_design_notes: Optional[str] = Field(default=None, description="Plan design notes or comments")

class DeductiblesAndCoinsurance(BaseModel):
    individual_deductible: Optional[str] = Field(description="In-Network Individual Deductible amount as string, keep $ and comma")
    family_deductible: Optional[str] = Field(description="In-Network Family Deductible amount as string, keep $ and comma")
    deductible_type: Optional[str] = Field(description="e.g. Embedded - Traditional Style, True Individual Family")
    individual_oop_max: Optional[str] = Field(description="Individual Out Of Pocket Maximum as string, keep $ and comma")
    family_oop_max: Optional[str] = Field(description="Family Out Of Pocket Maximum as string, keep $ and comma")
    family_tier: Optional[str] = Field(default=None, description="Calculated Family Tier multiplier (e.g. 2x, 2.5x, 3x)")
    in_network_coinsurance: Optional[str] = Field(default=None, description="The most frequent coinsurance percentage in the In-Network column")

